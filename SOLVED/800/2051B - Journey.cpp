void solve()
{
    ll n, a, b, c;
    cin >> n >> a >> b >> c;
    ll ans = (n / (a + b + c));
    ll rem = n - ans * (a + b + c);
    ans *= 3;
    if(!rem)
            ;
    else if(rem <= a)
        ans ++;
    else if(rem <= a + b)
        ans += 2;
    else 
        ans += 3;
    cout << ans << endl;

}


